export async function GET(req: Request) {
  const url = new URL(req.url);
  const startDate = url.searchParams.get("startDate");
  const endDate = url.searchParams.get("endDate");
  const dataTypes = url.searchParams.get("dataTypes")?.split(",");

  if (!startDate || !endDate || !dataTypes || dataTypes.length === 0) {
    return NextResponse.json({ error: "Invalid parameters" }, { status: 400 });
  }

  // Filter the dummy data based on selected dataTypes
  const filteredData: DataEntry[] = dummyData.map((entry) => {
    let filteredEntry: Record<string, string | number> = { time: entry.time };

    dataTypes.forEach((key) => {
      if (validKeys.includes(key as keyof DataEntry)) {
        // Ensure TypeScript correctly assigns values
        filteredEntry[key] = entry[key as keyof DataEntry] as number;
      }
    });

    return filteredEntry as DataEntry; // Cast back to DataEntry
  });

  return NextResponse.json({ data: filteredData });
}
